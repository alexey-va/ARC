package ru.arc.audit

/**
 * Типы экономических операций для аудита.
 *
 * Каждый тип представляет источник или назначение денежной транзакции.
 */
enum class Type {
    /** Покупка/продажа в NPC магазине */
    SHOP,

    /** Заработок от работы (Jobs Reborn) */
    JOB,

    /** Перевод между игроками (/pay) */
    PAY,

    /** Выполнение команды (награды, штрафы) */
    COMMAND,

    /** Покупка/продажа в ChestShop */
    CHEST_SHOP,

    /** Дивиденды от акций */
    DIVIDEND,

    /** Покупка/продажа акций */
    STOCK,

    /** Аукцион */
    AUCTION,

    /** Прочие операции */
    OTHER
}


